#include "fdf.h"

int		ft_checkcolor(char *s)
{
	int		i;
	i = 3;
	if (s[0] == ',' && s[1] && s[1] == '0'
			&& s[2] && s[2] == 'x' && s[3] && s[3] != ' ' && s[3] != '\t')
	{
		while (s[i] &&  s[i] != ' ' && s[i] != '\t' && (ft_isdigit(s[i])
					|| (s[i] >= 'a' && s[i] <= 'f')
					|| (s[i] >= 'A' && s[i] <= 'F')))
			i++;
		if (!s[i] || (s[i] && (s[i] == ' ' || s[i] == '\t')))
			return (1);
	}
	return (0);
}
